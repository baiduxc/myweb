// 六十甲子
export function getJiaZi(): string[] {
  const jz: string[] = [];
  for (let i = 0; i < 60; i++) {
    jz.push(tianGan[i % 10] + diZhi[i % 12]);
  }
  return jz;
}

// 获取某干支在六十甲子中的序号
export function getJiaZiIndex(gan: string, zhi: string): number {
  for (let i = 0; i < 60; i++) {
    if (tianGan[i % 10] === gan && diZhi[i % 12] === zhi) {
      return i;
    }
  }
  return 0;
}

