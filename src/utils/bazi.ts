export function getShiChenName(hour: number): string {
  if (hour === 23 || hour === 0) return '子';
  for (const sc of shiChen) {
    if (hour >= sc.start && hour < sc.end) {
      return sc.name.replace('时', '');
    }
  }
  return '子';
}
